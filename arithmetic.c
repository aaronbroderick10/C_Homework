#include <stdio.h>

int main() {
    
    //1
    int x = 0;
    printf("x: %d\n", x);   
    printf("x++: %d\n", x++);   
    x = 0;
    printf("x: %d\n", x);   
    printf("++x: %d\n", ++x);   
    x = 0;
    printf("x: %d\n", x);   
    printf("x--: %d\n", x--);   
    x = 0;
    printf("x: %d\n", x);   
    printf("--x: %d\n", --x);   

    return 0;
}
